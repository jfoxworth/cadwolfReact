import { Equation } from '../eqSolver'
//-------------------------------------------------------------------------------------------------------------------------------------//
//---------------------------------------------------- GET EQUATION INDEX --------------------------------------------------------------//
//-------------------------------------------------------------------------------------------------------------------------------------//
/*	This function takes in an ID for an equation and returns the index within the DOM object for that equation
//-------------------------------------------------------------------------------------------------------------------------------------*/
export default function getEquationIndex(this: Equation, eqId: string) {
  return this.DOMObject.findIndex(eq => eq.equationId === eqId)
}
